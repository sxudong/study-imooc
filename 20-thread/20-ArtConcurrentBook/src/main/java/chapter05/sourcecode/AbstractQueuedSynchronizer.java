    /**
     * Releases in exclusive mode.  Implemented by unblocking one or
     * more threads if {@link #tryRelease} returns true.
     * This method can be used to implement method {@link Lock#unlock}.
     *
     * @param arg the release argument.  This value is conveyed to
     *        {@link #tryRelease} but is otherwise uninterpreted and
     *        can represent anything you like.
     * @return the value returned from {@link #tryRelease}
     *
     * release()独占式释放同步状态,释放线程后唤醒节点。
     *
     * 其中 tryRelease 也需要同步组件自己去实现，语义也被 AQS 所定义，true 代表释放成功，
     * false 代表失败，如果为 true，就需要决定是否去唤醒节点，首先获取同步队列的头节点，
     * 判断头结点不是空，证明有同步对别有节点才需要唤醒，判断头结点不是刚刚初始化，如
     * 果是刚刚初始化，就还没有阻塞，请参考 acquire 的 acquireQueued 处理节点的逻辑，都
     * 为 true 执行 unparkSuccessor 方法，false返回。
     */
    public final boolean release(int arg) {
        // 释放锁,子类实现 tryRelease() 释放同步状态
        if (tryRelease(arg)) {
            // 获取头结点
            Node h = head;
            // 头结点不为空，证明初始化了
            // 证明头结点不是刚刚创建
            // 那就可以去唤醒头结点或者它的后继节点
            // 为 0 就证明没有其他节点了，不需要唤醒
            if (h != null && h.waitStatus != 0)
                unparkSuccessor(h);
            return true;
        }
        return false;
    }

